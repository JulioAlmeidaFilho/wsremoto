package decisao;

import javax.swing.JOptionPane;

public class DesafioDecisaoL2Ex1 {
	
public static void main(String[] args) {
	
	/*
	 * 1. Um hotel cobra R$ 80,00 a diária e mais uma taxa de serviços. 
A taxa de serviços é de: 
•	R$ 5,50 por diária, se o número de diárias for maior que 15; 
•	R$ 6,00 por diária, se o número de diárias for igual a 15; 
•	R$ 8,00 por diária, se o número de diárias for menor que 15.
Monte uma aplicação que apresente a conta do cliente.

	 */

	short diarias = Short.parseShort(JOptionPane.showInputDialog("Digite o número de diarias"));

	if (diarias>15) {
	System.out.println("O valor total é de R$ "+(80+5.5)*diarias);
	} else if (diarias ==15) {
	System.out.println("O valor total é de R$ "+(80+6)*diarias);
	}else {
	System.out.println("O valor total é de R$ "+(80+8)*diarias);
	}
		
}
}
